
// JavaScript Arithmetic Operators

/*
Operator	Description
    +	    Addition
    -	    Subtraction
    *	    Multiplication
    **	    Exponentiation (ES2016)
    /	    Division
    %	    Modulus (Remainder)
    ++	    Increment
    --	    Decrement   
*/

// Example

let x = 100 + 50;
console.log(x);

// Example
let y = (10 + 20) * 3;
console.log(y);


// Adding
// The addition operator (+) adds numbers:
var a = 5;
var b = 2;
var z = a + b;
console.log("Adding : ", z);


// Subtracting
// The addition operator (-) adds numbers:
var a = 5;
var b = 2;
var z = a - b;
console.log("Subtracting : ", z);


// Multiplying
// The addition operator (*) adds numbers:
var a = 5;
var b = 2;
var z = a * b;
console.log("Multiplying : ", z);


// Dividing
// The addition operator (/) adds numbers:
var a = 5;
var b = 2;
var z = a / b;
console.log("Dividing : ",z);

// Remainder
// The addition operator (%) adds numbers:
var a = 5;
var b = 2;
var z = a % b;
console.log("Remainder : ", z);


// Incrementing
// The addition operator (++) adds numbers:
var a = 5;
a++;
b = a;
console.log("Incrementing : ", b);


// Decrementing
// The addition operator (--) adds numbers:
var a = 5;
a--;
b = a;
console.log("Decrementing : ", b);

// Exponentiation
// The exponentiation operator (**) raises the first operand to the power of the second operand.
var a = 5;
var b = a ** 2;
console.log("Exponentiation : ", b);