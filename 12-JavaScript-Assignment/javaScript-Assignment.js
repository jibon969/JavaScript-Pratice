
// JavaScript Assignment

/*
JavaScript Assignment Operators
    Operator	    Example	        Same As
        =	        x = y	        x = y
        +=	        x += y	        x = x + y
        -=	        x -= y	        x = x - y
        *=	        x *= y	        x = x * y
        /=	        x /= y	        x = x / y
        %=	        x %= y	        x = x % y
        <<=	        x <<= y	        x = x << y
        >>=	        x >>= y	        x = x >> y
        >>>=	    x >>>= y	    x = x >>> y
        &=	        x &= y	        x = x & y
        ^=	        x ^= y	        x = x ^ y
        |=	        x |= y	        x = x | y
        **=	x       **= y	        x = x ** y
*/

// Assignment Examples

